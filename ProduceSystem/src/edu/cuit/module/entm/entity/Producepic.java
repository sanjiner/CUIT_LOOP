package edu.cuit.module.entm.entity;

import java.util.Date;

public class Producepic implements java.io.Serializable{
	private String id;
	private String applyBh;
	private String picName;
	private String picContent;
	private String  time;
	private String picsrc;
	

	public Producepic() {
	}
	public Producepic(String id, String applyBh, String picName,
			String picContent, String time, String picsrc) {
		this.id = id;
		this.applyBh = applyBh;
		this.picName = picName;
		this.picContent = picContent;
		this.time = time;
		this.picsrc = picsrc;
	}
	
	public Producepic(String applyBh, String picName, String picContent,
			String time, String picsrc) {
		super();
		this.applyBh = applyBh;
		this.picName = picName;
		this.picContent = picContent;
		this.time = time;
		this.picsrc = picsrc;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getApplyBh() {
		return applyBh;
	}
	public void setApplyBh(String applyBh) {
		this.applyBh = applyBh;
	}
	public String getPicName() {
		return picName;
	}
	public void setPicName(String picName) {
		this.picName = picName;
	}
	public String getPicContent() {
		return picContent;
	}
	public void setPicContent(String picContent) {
		this.picContent = picContent;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getPicsrc() {
		return picsrc;
	}
	public void setPicsrc(String picsrc) {
		this.picsrc = picsrc;
	}

}
